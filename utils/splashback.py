class  Splashback:
    def __init__(self,m200_bins,rsp_bins):
        self.m200_bins = m200_bins
        self.rsp_bins = rsp_bins

    def interp(self,m200):
        return np.interp(np.log10(m200), np.log10(self.m200_bins), self.rsp_bins)

